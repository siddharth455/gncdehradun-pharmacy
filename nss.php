                <h1>The NSS (National Service Scheme)</h1>
                <p>The NSS (National Service Scheme) operates under the Ministry of Youth Affairs and Sports of the Government of India. It aims to inculcate the spirit of social service and volunteerism among students, encouraging them to actively contribute to the welfare of society. The program operates on the principle of "Not Me, But You" emphasizing the importance of selfless service and collective action.</p>
                <p>The scheme was launched in 1969, with the aim of fostering social consciousness, promoting community service, and developing the personality of students through active participation in various social welfare activities.</p>
                <p>The symbol of the NSS is designed based on ‘Rath wheel’ of Konark, the Sun Temple of Odisha. The wheel signifies the progressive cycle of life. The symbol of NSS Badge contains 24 wheels in the Rath of the Sun Temple of Konark. Each Wheel has eight bars which represents 8 pahars of day. The red colour in the badge indicates that the NSS volunteers are full of blood, i.e. lively, active and spirited. The navy blue color indicates the cosmos of which the NSS is a tiny part ready to contribute its share for the welfare of the mankind.</p>
                <p>The NSS is implemented as a student-centered program in colleges and universities across the country. It encourages students to engage in community service and participate in activities that address social issues such as literacy, health and hygiene, environmental conservation, rural development, and social justice. The program promotes the idea of empathy, social responsibility, and active citizenship among young people.</p>
                <p><strong>The NSS Unit of Guru Nanak College of Pharmaceutical was established on March 19, 2020.</strong> This unit is self-financed, and only, 30 students were allotted initially. A lot of programs have been organized by the NSS unit on a timely basis.</p>
                <p>NSS activities are carried out through regular camps and ongoing projects. During these programs, student volunteers work actively to bring about social change and development.</p>
