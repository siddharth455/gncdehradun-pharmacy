        </ul>
        <h4>Ethical Principles in Research:</h4>
        <ul class="list-unstyled p-0 list-icon-4 ps-4 mt-3">
            <li><b>Voluntary Participation (Informed Consent):</b> Subjects enrolled in research must agree to reveal information about them and be informed about the treatment they will undergo before agreeing.</li>
            <li><b>Non-Maleficence (No Harm to Subjects):</b> Subjects should be free from reasonably anticipated physical or emotional harm.</li>
            <li><b>Privacy and Confidentiality:</b> Researchers should keep all information acquired in professional practice confidential. Disclosure should only occur with consent, when the law demands, or to protect patients or others from harm. Compliance with the consumer’s right to privacy is essential.</li>
            <li><b>Beneficence (Promotion of Well-Being):</b> Researchers have a duty to minimize harm and maximize benefits.</li>
            <li><b>Respect for Consumer’s Choice:</b> Researchers should respect the consumer’s choice, including the right to refuse treatment, care, or advice, or to withdraw consent at any time.</li>
            <li><b>Principle of Justice:</b> Researchers should treat all subjects equally without discrimination and respect their right to fair treatment and privacy.</li>
            <li><b>Respect for Dignity of Patients or Clients:</b> Researchers should respect the dignity of patients or clients with diminished competence and seek to involve them in decisions regarding their health.</li>
            <li><b>Approval from Regulatory Bodies:</b> Researchers should not conduct research on human beings unless the research proposal has been approved by the appropriate ethics committee.</li>
            <li><b>Respect for Patients’ Autonomy:</b> Researchers must encourage and empower patients, or in certain circumstances their carers/guardians, to actively participate in decisions about their health and care.</li>
            <li><b>Quality:</b> Researchers should maintain quality, accuracy, and reliability in their research. The highest quality standards should be applied for planning, implementation, and reporting of research.</li>
        </ul>
